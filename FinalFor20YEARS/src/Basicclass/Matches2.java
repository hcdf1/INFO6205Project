package Basicclass;

public class Matches2 {
	public int round;
	public int homeTeamNo;
	public int AwayTeamNo;
	public int homegoal;
	public int awaygoal;
	public int win;
	
    
    public Matches2()
    {
    	
    }


	public int getRound() {
		return round;
	}


	public void setRound(int round) {
		this.round = round;
	}


	public int getHomeTeamNo() {
		return homeTeamNo;
	}


	public void setHomeTeamNo(int homeTeamNo) {
		this.homeTeamNo = homeTeamNo;
	}


	public int getAwayTeamNo() {
		return AwayTeamNo;
	}


	public void setAwayTeamNo(int awayTeamNo) {
		AwayTeamNo = awayTeamNo;
	}


	public int getHomegoal() {
		return homegoal;
	}


	public void setHomegoal(int homegoal) {
		this.homegoal = homegoal;
	}


	public int getAwaygoal() {
		return awaygoal;
	}


	public void setAwaygoal(int awaygoal) {
		this.awaygoal = awaygoal;
	}


	public int getWin() {
		return win;
	}


	public void setWin(int win) {
		this.win = win;
	}
    
	

}
